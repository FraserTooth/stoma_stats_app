  Future<String> _signInWithEmailAndPassword(LoginData data) async {
    try {
      UserCredential userCredential =
          await FirebaseAuth.instance.signInWithEmailAndPassword(
        email: data.name,
        password: data.password,
      );
      setState(() {
        _firebaseUser = userCredential;
      });
    } on FirebaseAuthException catch (e) {
      if (e.code == 'user-not-found') {
        return 'No user found for that email.';
      } else if (e.code == 'wrong-password') {
        return 'Wrong password provided for that user.';
      }
    } catch (e) {
      print(e);
      return e.code;
    }
    return null;
  }

  Future<String> _signUpWithEmailAndPassword(LoginData data) async {
    try {
      UserCredential userCredential =
          await FirebaseAuth.instance.createUserWithEmailAndPassword(
        email: data.name,
        password: data.password,
      );
      setState(() {
        _firebaseUser = userCredential;
      });
    } on FirebaseAuthException catch (e) {
      if (e.code == 'weak-password') {
        return 'The password provided is too weak.';
      } else if (e.code == 'email-already-in-use') {
        return 'The account already exists for that email.';
      }
    } catch (e) {
      print(e);
      return e.code;
    }
    return null;
  }

  Future<String> _recoverPassword(String name) {
    try {
      FirebaseAuth.instance.sendPasswordResetEmail(email: name);
    } catch (e) {
      print(e);
      return e.code;
    }
    return null;
  }

